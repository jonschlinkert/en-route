'use strict';

/**
 * Module dependencies.
 */

var debug = require('debug')('router:route');
var slice = require('array-slice');
var flatten = require('arr-flatten');
var Layer = require('./layer');

/**
 * Expose `Route`.
 */

module.exports = Route;

/**
 * Initialize `Route` with the given `path`,
 *
 * @param {String} path
 * @api private
 */

function Route(path) {
  debug('new %s', path);
  this.path = path;
  this.stack = [];
}

/**
 * dispatch file into this route
 *
 * @api private
 */

Route.prototype.dispatch = function dispatch(file, done) {
  var idx = 0;
  var stack = this.stack;
  if (stack.length === 0) {
    return done();
  }

  file.route = this;
  next();

  function next(err) {
    if (err && err === 'route') {
      return done();
    }

    var layer = stack[idx++];
    if (!layer) {
      return done(err);
    }

    if (err) {
      layer.handleError(err, file, next);
    } else {
      layer.handleFile(file, next);
    }
  }
};

/**
 * Add a handler for all HTTP verbs to this route.
 *
 * Behaves just like middleware and can respond or call `next`
 * to continue processing.
 *
 * You can use multiple `.all` call to add multiple handlers.
 *
 *   function check_something(req, res, next) {
 *     next();
 *   };
 *
 *   function validate_user(req, res, next) {
 *     next();
 *   };
 *
 *   route
 *     .all(validate_user)
 *     .all(check_something)
 *     .get(function(req, res, next) {
 *       res.send('hello world');
 *     });
 *
 * @param {function} handler
 * @return {Route} for chaining
 * @api public
 */

Route.prototype.all = function all() {
  var callbacks = flatten(slice(arguments));

  callbacks.forEach(function(fn) {
    if (typeof fn !== 'function') {
      var type = {}.toString.call(fn);
      var msg = 'Route.all() requires callback functions but got a ' + type;
      throw new Error(msg);
    }
    var layer = Layer('/', {}, fn);
    this.stack.push(layer);
  }, this);
  return this;
};
